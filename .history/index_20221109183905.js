
function add(a, b) {
    const val = a + b;
    return val;
}

function subtract(a, b) {
    const val = a - b;
    return val;
}

function multiply(a, b) {
    const val = a * b;
    return val;
}

function divide(a, b) {
    const val = a / b;
    return val;
}

function increment(n) {
    const val = n + 1;
    return val;
}

function decrement(n) {
    const val = n - 1;
    return val;
}